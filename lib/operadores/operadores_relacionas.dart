void main() {
  final idade = 18;

  //* == -> igual
  if (idade == 18) {
    print('pode tirar habilitação');
  }

  //* != -> diferente
  if (idade != 18) {
    print('pode tirar habilitação');
  }

  //* >= -> maior igual
  if (idade >= 18) {
    print('pode tirar habilitação');
  }

  //* <= -> menor igual
  if (idade <= 18) {
    print('pode tirar habilitação');
  }

  //* > -> maior que
  if (idade > 18) {
    print('pode tirar habilitação');
  }

  //* < -> menor que
  if (idade < 18) {
    print('pode tirar habilitação');
  }
}
